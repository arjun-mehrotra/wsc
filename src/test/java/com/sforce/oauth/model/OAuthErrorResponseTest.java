package com.sforce.oauth.model;

import com.fasterxml.jackson.databind.DeserializationFeature;
import com.fasterxml.jackson.databind.ObjectMapper;
import org.junit.Before;
import org.junit.Test;

import java.io.IOException;

import static org.junit.Assert.assertEquals;
import static org.junit.Assert.assertNotNull;

public class OAuthErrorResponseTest {

    private static final String JSON = "{\"error\": \"invalid_grant\", \"error_description\": \"grant type not supported\", \"extra_field\": 123}";
    private ObjectMapper mapper;

    @Before
    public void setup() {
        mapper = new ObjectMapper();
        mapper.configure(DeserializationFeature.FAIL_ON_UNKNOWN_PROPERTIES, false);
    }

    @Test
    public void testJsonDeserialization() throws IOException {
        OAuthErrorResponse response = mapper.readValue(JSON, OAuthErrorResponse.class);

        assertNotNull(response);
        assertEquals("'error' property not mapped correctly","invalid_grant", response.getError());
        assertEquals("'error_description' property not mapped correctly", "grant type not supported", response.getErrorDescription());
    }
}